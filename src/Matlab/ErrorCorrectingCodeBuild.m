        '@TurboCode/TurboCode_constructor.cpp'; ...
        '@LdpcCode/LdpcCode_constructor.cpp'
        };
    
    
    
oldpath = cd(strrep(which(fullfile('src', 'Matlab', 'ErrorCorrectingCodeBuild.m')), fullfile('ErrorCorrectingCodeBuild.m'), ''));
    mkdir build
    mkdir product
    
    objs = '';
    for i = 1:length(src)
        [pathstr,name,ext] = fileparts(src{i});
        obj = ['build/' name '.o'];
        objs{i} = obj;
        
        objInfo = dir(obj);
        srcInfo = dir([srcPath src{i}]);
        if (isempty(objInfo) || objInfo.datenum < srcInfo.datenum)
            mex(['CXXFLAGS="\$CXXFLAGS ' cxxFlags '"'], iPath, '-outdir', objDst, '-c', [srcPath src{i}]);
        else
            disp('skip');
        end
    end
    
    for i = 1:length(trg)
        mex(['CXXFLAGS="\$CXXFLAGS ' cxxFlags '"'], iPath, '-largeArrayDims', '-outdir', trgDst, [trgPath trg{i}], objs{:});
    end
    

    %delete build/*
    %rmdir build
    cd(oldpath)
end
    