        'libs/serialization/src/xml_wiarchive.cpp'; ...
        'libs/serialization/src/xml_woarchive.cpp'; ...
        'libs/serialization/src/xml_wgrammar.cpp'; ...
        };
    
    trg = {...
        'nullHandle.cpp'; ...
        '@ErrorCorrectingCode/ErrorCorrectingCode_get_paritySize.cpp'; ...
        '@ErrorCorrectingCode/ErrorCorrectingCode_get_messageSize.cpp'; ...
